# > Operações matemáticas (Aritméticas)

"""
1. Adição (+)
2. Subtração (-)
3. Multiplicação (*)
4. Divisão (/)
5. Divisão inteira (//)
6. Resto da divisão (%)
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 // 3)
print(20 % 3)
print(2 ** 3)

