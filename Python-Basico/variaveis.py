# 1. Variáveis

idade = 26

print(idade)

"""
    TIPOS DE variáveis
    1. int: números inteiros, ou seja, números sem parte decimal: 0, 5, -1, 1000
    2. float: números reais, ou seja, números com parte decimal: 1.0, -2.7, 3.14
    3. str: strings, ou seja, textos: "Olá", "Python", "100", dados textuais
    4. bool: booleanos, ou seja, valores lógicos: True, False
"""    

idade = 40
altura = 1.80
nome = "Philipe Oliveira"
estudando = True

print( type(idade) )
print( type(altura) )
print( type(nome) )
print( type(estudando) )

